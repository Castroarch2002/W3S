#include <iostream> /* #include <iostream> is a header file library that works with 
						input and output objects, such as cout. Header files add functionality to C++ programs */

using namespace std; /* using namespace std means that we use names for objects and variables from the standard library */

int main() {		/* This is called a function, Any code inside {} is executed */
	cout << "Hello World!" << endl;	// This is a comment
	cout << "I am learning C++";	/* An object used together with the insertion opoerator to output text*/
	return 0;				/* return 0 ends the main function*/
}						/* the curly bracket actually ends the main function */
						/* This is a multi line comment */
